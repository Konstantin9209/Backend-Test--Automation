function calculateSumAndSubtract(num1, num2, num3) {
    const result = (num1 + num2) - num3;
    return result;
}

const example1 = calculateSumAndSubtract(23, 6, 10);
console.log(example1);

const example2 = calculateSumAndSubtract(1, -12, 17);
console.log(example2); 

const example3 = calculateSumAndSubtract(42, 58, 100);
console.log(example3); 
